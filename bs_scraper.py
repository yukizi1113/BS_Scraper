def process_one_zip(zip_bytes: bytes, doc_kind: Optional[str] = None) -> Dict[str, object]:
    ctx_info, facts, html_candidates = parse_zip_any(zip_bytes)

    bs_full = set(sum(BS_FIELDS.values(), []))
    extra_full = set(
        IB_LIAB_CL_TOTAL_CONCEPTS + IB_LIAB_NCL_TOTAL_CONCEPTS
        + BORROWINGS_CL_TOTAL_CONCEPTS + BORROWINGS_NCL_TOTAL_CONCEPTS
        + ST_BORROWINGS_DIRECT_CONCEPTS + ST_BORROWINGS_CURRENT_PORTION_CONCEPTS + LT_BORROWINGS_DIRECT_CONCEPTS
        + BONDS_CL_TOTAL_CONCEPTS + BONDS_NCL_TOTAL_CONCEPTS
        + LEASE_CL_TOTAL_CONCEPTS + LEASE_CL_COMPONENT_CONCEPTS + LEASE_NCL_TOTAL_CONCEPTS + LEASE_NCL_COMPONENT_CONCEPTS
        + COMMERCIAL_PAPERS_CL_CONCEPTS
        + SHARES_CONCEPTS
        + CASH_EQ_ST_INV_TRUE_TOTAL_CONCEPTS + CASH_DEPOSITS_CONCEPTS + CASH_EQ_FALLBACK_CONCEPTS
        + CASH_EQ_CALL_LOANS_CONCEPTS + CASH_EQ_NONOP_SECURITIES_TOTAL_CONCEPTS + CASH_EQ_OPERATIONAL_SECURITIES_CONCEPTS
    )
    bs_local = set(local_name(x) for x in (bs_full | extra_full))

    prior_date_str, prior_ctx, cur_date_str, cur_ctx = choose_bs_contexts(
        facts, ctx_info, bs_full | extra_full, bs_local
    )

    v_full, v_local = facts_to_value_maps(facts)

    out: Dict[str, object] = {
        "bs_prior_date": prior_date_str,
        "bs_current_date": cur_date_str,
        "fy_end_month": extract_fy_end_month_from_dei(facts),
    }

    for field, concepts in BS_FIELDS.items():
        out[f"{field}_prior"] = first_value(v_full, v_local, concepts, prior_ctx)
        out[f"{field}_current"] = first_value(v_full, v_local, concepts, cur_ctx)

    out["cash_eq_st_invest_prior"] = calc_cash_eq_st_invest(v_full, v_local, prior_ctx)
    out["cash_eq_st_invest_current"] = calc_cash_eq_st_invest(v_full, v_local, cur_ctx)

    prior_d = parse_date_any(prior_date_str) if prior_date_str else None
    cur_d = parse_date_any(cur_date_str) if cur_date_str else None

    prior_ctx_debt = pick_best_debt_context(prior_d, prior_ctx, ctx_info, v_full, v_local)
    cur_ctx_debt   = pick_best_debt_context(cur_d, cur_ctx, ctx_info, v_full, v_local)

    # log用（WARN時に追えるように）
    out["debt_ctx_prior"] = prior_ctx_debt
    out["debt_ctx_current"] = cur_ctx_debt

    # debt components for each suffix (for rescue判断)
    for suffix, dctx in [("prior", prior_ctx_debt), ("current", cur_ctx_debt)]:
        comps = extract_debt_components_xbrl(v_full, v_local, dctx)
        for k in ["loans_cl", "bonds_cl", "cp_cl", "lease_cl", "st_total", "loans_ncl", "bonds_ncl", "lease_ncl", "lt_total"]:
            out[f"{k}_{suffix}"] = comps.get(k)

    out["short_term_borrowings_prior"] = out.get("st_total_prior")
    out["long_term_borrowings_prior"] = out.get("lt_total_prior")
    out["short_term_borrowings_current"] = out.get("st_total_current")
    out["long_term_borrowings_current"] = out.get("lt_total_current")

    out["shares_outstanding_prior"] = extract_shares(v_full, v_local, prior_ctx)
    out["shares_outstanding_current"] = extract_shares(v_full, v_local, cur_ctx)

    # ASRのみHTML補完（既存安定版）
    if doc_kind == "ASR" and html_candidates and cur_ctx_debt:
        _apply_html_additive_adjustment_for_asr(out, v_full, v_local, cur_ctx_debt, html_candidates)

    return out


