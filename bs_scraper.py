def parse_fy_q(label: object) -> Optional[Tuple[int, int]]:
    if label is None:
        return None
    s = str(label).strip()
    m = re.search(r"(\d{4})年度第([1-4])四半期", s)
    if not m:
        return None
    return int(m.group(1)), int(m.group(2))


def build_col_map(ws, header_top: str) -> Dict[Tuple[int, int], int]:
    mp: Dict[Tuple[int, int], int] = {}
    for col in range(1, ws.max_column + 1):
        if str(ws.cell(1, col).value).strip() != header_top:
            continue
        fyq = parse_fy_q(ws.cell(2, col).value)
        if fyq:
            mp[fyq] = col
    return mp


def infer_fy_end_month_from_excel(ws, row: int, date_col_map: Dict[Tuple[int, int], int]) -> Optional[int]:
    q4_months: List[int] = []
    candidates: List[int] = []
    for (_fy, q), col in date_col_map.items():
        v = ws.cell(row, col).value
        ym = parse_yyyymm(v)
        if not ym:
            continue
        _y, m = ym
        end_m = ((m + (4 - q) * 3 - 1) % 12) + 1
        candidates.append(end_m)
        if q == 4:
            q4_months.append(m)
    if q4_months:
        return Counter(q4_months).most_common(1)[0][0]
    if candidates:
        return Counter(candidates).most_common(1)[0][0]
    return None


def fiscal_year_and_quarter(period_end_date: dt.date, fy_end_month: int) -> Tuple[int, int]:
    start_month = (fy_end_month % 12) + 1
    fy = period_end_date.year if (start_month == 1 or period_end_date.month >= start_month) else period_end_date.year - 1
    offset = (period_end_date.month - start_month) % 12
    q = (offset // 3) + 1
    return fy, max(1, min(4, q))


def collect_rows_and_tickers(path: str, max_rows: Optional[int]) -> Tuple[List[int], set[str]]:
    wb = load_workbook(path, read_only=True)
    ws = wb.active
    rows: List[int] = []
    tickers: set[str] = set()
    count = 0
    for r in range(4, ws.max_row + 1):
        v = ws.cell(r, 1).value
        if v is None:
            continue
        m = re.search(r"(\d{4})", str(v))
        if not m:
            continue
        rows.append(r)
        tickers.add(m.group(1))
        count += 1
        if max_rows is not None and count >= max_rows:
            break
    return rows, tickers


def fill_excel_from_store(
    input_xlsx: str,
    output_xlsx: str,
    store: Dict[str, Dict[dt.date, dict]],
    rows_to_process: List[int],
    warn_tolerance: Decimal = Decimal("1.0"),
    updated_color: str = "FFFFFF00",
    warning_color: str = "FFFFC000",
    verbose_warnings: bool = True,
) -> None:
    YELLOW = PatternFill(fill_type="solid", start_color=updated_color, end_color=updated_color)
    ORANGE = PatternFill(fill_type="solid", start_color=warning_color, end_color=warning_color)

    wb = load_workbook(input_xlsx)
    ws = wb.active

    CHECK_COL = column_index_from_string("EE")
    ws.cell(1, CHECK_COL).value = "懸念check"
    ws.cell(2, CHECK_COL).value = ws.cell(2, CHECK_COL).value or ""
    ws.cell(3, CHECK_COL).value = ws.cell(3, CHECK_COL).value or ""

    date_cols = build_col_map(ws, "決算期")
    col_assets = build_col_map(ws, "資産合計")
    col_cur_assets = build_col_map(ws, "流動資産")
    col_cash = build_col_map(ws, "現金同等物及び短期性有価証券")
    col_cur_liab = build_col_map(ws, "流動負債")
    col_st_debt = build_col_map(ws, "短期借入債務")
    col_lt_debt = build_col_map(ws, "長期借入債務")
    col_shares = build_col_map(ws, "期末発行済株式数 - 普通株")
    col_tax = build_col_map(ws, "未払法人税")

    field_to_cols = {
        "assets_total": col_assets,
        "current_assets": col_cur_assets,
        "cash_eq_st_invest": col_cash,
        "current_liabilities": col_cur_liab,
        "short_term_borrowings": col_st_debt,
        "long_term_borrowings": col_lt_debt,
        "shares_outstanding": col_shares,
        "income_taxes_payable": col_tax,
    }

    # 仕様変更：EE列「懸念check」は“直近5四半期”の橙色セル有無で判定する
    sorted_fyq = sorted(date_cols.keys())
    last5_fyq = set(sorted_fyq[-5:]) if len(sorted_fyq) >= 5 else set(sorted_fyq)

    warnings: List[str] = []
    filled = 0
    compared = 0
    warn_cells = 0
    row_has_warning_last5: Dict[int, bool] = defaultdict(bool)

    for row in rows_to_process:
        code_v = ws.cell(row, 1).value
        if code_v is None:
            continue
        m = re.search(r"(\d{4})", str(code_v))
        if not m:
            continue
        code4 = m.group(1)

        recs = store.get(code4)
        if not recs:
            ws.cell(row, CHECK_COL).value = ""
            continue

        excel_fy_end_month = infer_fy_end_month_from_excel(ws, row, date_cols)

        for period_end, rec in recs.items():
            fy_end_month = rec.get("fy_end_month")
            if not isinstance(fy_end_month, int) or not (1 <= fy_end_month <= 12):
                fy_end_month = excel_fy_end_month or 3

            fy, q = fiscal_year_and_quarter(period_end, fy_end_month)

            dcol = date_cols.get((fy, q))
            if dcol is not None:
                c = ws.cell(row, dcol)
                if is_blank_cell(c.value):
                    c.value = f"{period_end.year}/{period_end.month:02d}"
                    c.fill = YELLOW
                    filled += 1

            for field, cmap in field_to_cols.items():
                col = cmap.get((fy, q))
                if col is None:
                    continue
                new_val = rec.get(field)
                if new_val is None or new_val == "":
                    continue

                cell = ws.cell(row, col)
                old_val = cell.value

                if is_blank_cell(old_val):
                    cell.value = new_val
                    if field != "shares_outstanding" and isinstance(new_val, (int, float)) and has_fraction_3dp(float(new_val)):
                        cell.number_format = "#,##0.000"
                    cell.fill = YELLOW
                    filled += 1
                else:
                    old_d = to_decimal_safe(old_val)
                    new_d = to_decimal_safe(new_val)
                    if old_d is None or new_d is None:
                        continue
                    compared += 1
                    diff = (old_d - new_d).copy_abs()
                    if diff > warn_tolerance:
                        cell.fill = ORANGE
                        warn_cells += 1
                        in_last5 = (fy, q) in last5_fyq
                        if in_last5:
                            row_has_warning_last5[row] = True
                        warnings.append(
                            f"WARN {code4} R{row} C{col} field={field} fyq={fy}Q{q} in_last5={in_last5} existing={old_val} new={new_val} diff={diff} "
                            f"(period_end={period_end.isoformat()}, as_of={rec.get('as_of')}, source={rec.get('source')}, doc_kind={rec.get('doc_kind')}, suffix={rec.get('suffix')}, debt_ctx={rec.get('debt_ctx')}, title={rec.get('title','')})"
                        )

        ws.cell(row, CHECK_COL).value = 1 if row_has_warning_last5.get(row, False) else ""

    wb.save(output_xlsx)

    print(f"Done. filled_cells={filled}, compared_existing={compared}, warnings={len(warnings)}, warning_cells={warn_cells}")
    if verbose_warnings and warnings:
        print("\n".join(warnings[:200]))
        if len(warnings) > 200:
            print(f"... and {len(warnings)-200} more warnings")


