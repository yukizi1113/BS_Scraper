def parse_date_any(s: object) -> Optional[dt.date]:
    # v24.11 安定版：period_end の誤パースは四半期マッピング崩れ→誤警告の最大要因なので堅めに。
    if s is None:
        return None
    t = str(s).strip()
    if not t:
        return None

    # 1) ISO / "YYYY-MM-DD..." (EDINET/TDNetで最頻)
    try:
        return dt.date.fromisoformat(t[:10])
    except Exception:
        pass

    # 2) "YYYY/MM/DD" / "YYYY-MM-DD"
    m = re.search(r"(\d{4})[-/](\d{1,2})[-/](\d{1,2})", t)
    if m:
        try:
            return dt.date(int(m.group(1)), int(m.group(2)), int(m.group(3)))
        except Exception:
            return None

    # 3) "YYYYMMDD"（8桁）
    m = re.search(r"(?<!\d)(\d{4})(\d{2})(\d{2})(?!\d)", t)
    if m:
        try:
            return dt.date(int(m.group(1)), int(m.group(2)), int(m.group(3)))
        except Exception:
            return None

    # 4) "YYYY年M月D日"
    m = re.search(r"(\d{4})年\s*(\d{1,2})月\s*(\d{1,2})日", t)
    if m:
        try:
            return dt.date(int(m.group(1)), int(m.group(2)), int(m.group(3)))
        except Exception:
            return None

    return None


def parse_yyyymm(s: object) -> Optional[Tuple[int, int]]:
    if s is None:
        return None
    t = str(s).strip()
    m = re.search(r"(\d{4})\s*[/\-]\s*(\d{1,2})", t)
    if not m:
        return None
    y = int(m.group(1))
    mo = int(m.group(2))
    return (y, mo) if 1 <= mo <= 12 else None


def parse_value_ixbrl(txt: str, scale: Optional[str], sign: Optional[str]) -> Optional[int]:
    if txt is None:
        return None
    s = str(txt).strip()
    if s == "" or s in {"—", "－", "-", "–", "―"}:
        return None
    neg = False
    if s.startswith(("△", "▲")):
        neg = True
        s = s[1:]
    if s.startswith("(") and s.endswith(")"):
        neg = True
        s = s[1:-1]
    s = s.replace(",", "").replace(" ", "").replace("\u3000", "")
    if s.startswith("-"):
        neg = True
        s = s[1:]
    if not re.search(r"\d", s):
        return None
    try:
        d = Decimal(s)
    except InvalidOperation:
        return None
    sc = int(scale) if scale not in (None, "") else 0
    d = d * (Decimal(10) ** sc)
    val = int(d.to_integral_value(rounding=ROUND_HALF_UP))
    return -val if (sign == "-" or neg) else val


