def parse_zip_any(zip_bytes: bytes) -> Tuple[Dict[str, dict], List[Fact], List[str]]:
    ctx_all: Dict[str, dict] = {}
    facts_all: List[Fact] = []
    html_candidates: List[str] = []

    parser = ET.XMLParser(recover=True, huge_tree=True)

    def parse_ixbrl_doc(xml_bytes: bytes) -> Tuple[Dict[str, dict], List[Fact]]:
        root = ET.fromstring(xml_bytes, parser=parser)

        ctxs: Dict[str, dict] = {}
        for ctx in root.xpath('//*[local-name()="context"]'):
            cid = ctx.get("id")
            if not cid:
                continue
            period = ctx.xpath('./*[local-name()="period"]')
            if not period:
                continue
            period = period[0]
            inst = period.xpath('./*[local-name()="instant"]/text()')
            if inst:
                ctxs[cid] = {"type": "instant", "instant_date": parse_date_any(inst[0].strip())}
                continue
            start = period.xpath('./*[local-name()="startDate"]/text()')
            end = period.xpath('./*[local-name()="endDate"]/text()')
            if start and end:
                ctxs[cid] = {"type": "duration", "start_date": parse_date_any(start[0].strip()), "end_date": parse_date_any(end[0].strip())}

        facts: List[Fact] = []
        for el in root.xpath('//*[local-name()="nonFraction" and namespace-uri()=$ix]', ix=IX_NS):
            name = el.get("name")
            if not name:
                continue
            facts.append(Fact(
                name=name,
                ctx=el.get("contextRef") or el.get("contextref"),
                scale=el.get("scale"),
                sign=el.get("sign"),
                txt="".join(el.itertext()).strip(),
                kind="ix_nonFraction",
            ))
        for el in root.xpath('//*[local-name()="nonNumeric" and namespace-uri()=$ix]', ix=IX_NS):
            name = el.get("name")
            if not name:
                continue
            facts.append(Fact(
                name=name,
                ctx=el.get("contextRef") or el.get("contextref"),
                scale=None,
                sign=None,
                txt="".join(el.itertext()).strip(),
                kind="ix_nonNumeric",
            ))
        return ctxs, facts

    def parse_xbrl_instance(xml_bytes: bytes) -> Tuple[Dict[str, dict], List[Fact]]:
        root = ET.fromstring(xml_bytes, parser=parser)

        ctxs: Dict[str, dict] = {}
        for ctx in root.xpath('//*[local-name()="context"]'):
            cid = ctx.get("id")
            if not cid:
                continue
            period = ctx.xpath('./*[local-name()="period"]')
            if not period:
                continue
            period = period[0]
            inst = period.xpath('./*[local-name()="instant"]/text()')
            if inst:
                ctxs[cid] = {"type": "instant", "instant_date": parse_date_any(inst[0].strip())}
                continue
            start = period.xpath('./*[local-name()="startDate"]/text()')
            end = period.xpath('./*[local-name()="endDate"]/text()')
            if start and end:
                ctxs[cid] = {"type": "duration", "start_date": parse_date_any(start[0].strip()), "end_date": parse_date_any(end[0].strip())}

        facts: List[Fact] = []
        for el in root.xpath('//*[@contextRef]'):
            if el.tag is None:
                continue
            ln = ET.QName(el).localname
            ctx = el.get("contextRef")
            txt = (el.text or "").strip()
            if el.get("unitRef") is not None:
                facts.append(Fact(name=ln, ctx=ctx, scale=None, sign=None, txt=txt, kind="xbrl_numeric"))
            else:
                if FISCAL_YEAR_END_LOCAL_RE.search(ln):
                    facts.append(Fact(name=ln, ctx=ctx, scale=None, sign=None, txt=txt, kind="xbrl_nonNumeric"))
        return ctxs, facts

    with zipfile.ZipFile(io.BytesIO(zip_bytes)) as zf:
        for info in zf.infolist():
            low = info.filename.lower()

            if low.endswith((".htm", ".html", ".xhtml")):
                b = zf.read(info)
                try:
                    text = b.decode("utf-8", "ignore")
                except Exception:
                    text = ""
                if "<table" in text:
                    html_candidates.append(text)

                if (b.find(b"nonFraction") != -1) and (b.find(b"xmlns:ix") != -1 or b.find(b"<ix:") != -1):
                    try:
                        ctxs, facts = parse_ixbrl_doc(b)
                        ctx_all.update(ctxs)
                        facts_all.extend(facts)
                    except Exception:
                        pass
                continue

            if low.endswith(".xbrl"):
                b = zf.read(info)
                if b"<xbrl" not in b[:5000].lower():
                    continue
                try:
                    ctxs, facts = parse_xbrl_instance(b)
                    ctx_all.update(ctxs)
                    facts_all.extend(facts)
                except Exception:
                    pass

    return ctx_all, facts_all, html_candidates


