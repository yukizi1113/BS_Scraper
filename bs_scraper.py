def _parse_int_from_cell(s: str) -> Optional[int]:
    if s is None:
        return None
    t = str(s).strip()
    if t == "" or t in {"－", "—", "-", "–", "―"}:
        return None
    neg = False
    if t.startswith("(") and t.endswith(")"):
        neg = True
        t = t[1:-1]
    t = t.replace(",", "").replace(" ", "").replace("\u3000", "")
    if t.startswith("-"):
        neg = True
        t = t[1:]
    if not re.search(r"\d", t):
        return None
    try:
        v = int(Decimal(t).to_integral_value(rounding=ROUND_HALF_UP))
    except Exception:
        return None
    return -v if neg else v


def _detect_unit_and_explicit(text: str) -> Tuple[str, bool]:
    if "百万円" in text:
        return "百万円", True
    if "千円" in text:
        return "千円", True
    if "円" in text:
        return "円", True
    return "百万円", False


def _raw_to_yen(raw: Optional[int], unit: str) -> Optional[int]:
    if raw is None:
        return None
    if unit == "百万円":
        return int(raw) * 1_000_000
    if unit == "千円":
        return int(raw) * 1_000
    if unit == "円":
        return int(raw)
    return int(raw) * 1_000_000


_ONE_YEAR_RE = re.compile(r"(１年|1年).*(以内|内)", re.I)
_EXCLUDE_RE  = re.compile(r"(除く|excluding)", re.I)
_LOAN_RE     = re.compile(r"(借入|ローン|Loans?)", re.I)
_LEASE_RE    = re.compile(r"リース(債務|負債)", re.I)
_CP_RE       = re.compile(r"コマーシャル|ＣＰ|CP", re.I)
_BOND_RE     = re.compile(r"(社債|転換社債|新株予約権付社債|CB)", re.I)

_HDR_START_RE = re.compile(r"(当期首残高|期首残高|前期末残高|前期末|前連結会計年度末|前事業年度末|期首)", re.I)
_HDR_END_RE   = re.compile(r"(当期末残高|期末残高|当期末|期末|当連結会計年度末|当事業年度末)", re.I)
_HDR_RATE_RE  = re.compile(r"(利率|％|%|平均利率)", re.I)

def _pick_date_columns(matrix: List[List[str]]) -> Tuple[Optional[int], Optional[int]]:
    if not matrix:
        return None, None

    max_cols = max(len(r) for r in matrix[:3]) if matrix else 0
    headers_by_col = []
    for c in range(max_cols):
        parts = []
        for r in matrix[:3]:
            if c < len(r):
                s = str(r[c]).strip()
                if s:
                    parts.append(s)
        headers_by_col.append(" ".join(parts))

    end_col = None
    start_col = None
    for c, h in enumerate(headers_by_col):
        if end_col is None and _HDR_END_RE.search(h):
            end_col = c
    for c, h in enumerate(headers_by_col):
        if start_col is None and _HDR_START_RE.search(h):
            start_col = c

    if end_col is not None:
        if start_col is not None and start_col != end_col:
            return start_col, end_col
        if end_col - 1 >= 0:
            return end_col - 1, end_col

    body = matrix[1:] if len(matrix) > 1 else []
    max_cols2 = max(len(r) for r in body) if body else max_cols
    if max_cols2 <= 0:
        return None, None

    header_text = {}
    for c in range(max_cols2):
        parts = []
        for r in matrix[:3]:
            if c < len(r):
                s = str(r[c]).strip()
                if s:
                    parts.append(s)
        header_text[c] = " ".join(parts)

    scores = []
    for c in range(max_cols2):
        cnt = 0
        for r in body:
            if c >= len(r):
                continue
            v = _parse_int_from_cell(r[c])
            if v is not None:
                cnt += 1
        penalty = 2 if _HDR_RATE_RE.search(header_text.get(c, "")) else 0
        scores.append((cnt - penalty, c))

    scores.sort(key=lambda x: (x[0], x[1]))
    best = [c for s, c in scores if s > 0]
    if not best:
        return None, None
    end_col = best[-1]
    start_col = best[-2] if len(best) >= 2 else None
    return start_col, end_col


def extract_debt_schedules_from_html(html: str) -> List[dict]:
    out = []
    if not html or "<table" not in html:
        return out
    if "借入金等明細表" not in html:
        return out

    soup = BeautifulSoup(html, "lxml")
    tables = soup.find_all("table")
    if not tables:
        return out

    for t in tables:
        txt = t.get_text(" ", strip=True)
        if "借入金等明細表" not in txt and ("短期借入金" not in txt and "長期借入金" not in txt):
            continue

        matrix = []
        for tr in t.find_all("tr"):
            row = [c.get_text(" ", strip=True) for c in tr.find_all(["th", "td"])]
            if row:
                matrix.append(row)
        if len(matrix) < 2:
            continue

        unit_hint, unit_explicit = _detect_unit_and_explicit(txt + " " + " ".join(matrix[0]))
        _, end_col = _pick_date_columns(matrix)
        if end_col is None:
            continue

        st_end = {}
        lt_end = {}

        for row in matrix[1:]:
            if len(row) <= end_col:
                continue
            label = row[0]
            v_end = _parse_int_from_cell(row[end_col])
            if v_end is None:
                continue

            exclude_flag = bool(_EXCLUDE_RE.search(label))
            one_year = bool(_ONE_YEAR_RE.search(label))

            if _CP_RE.search(label):
                st_end["cp"] = v_end
                continue

            if "短期借入" in label:
                st_end["short_loan"] = int(st_end.get("short_loan") or 0) + int(v_end)
                continue

            if _LEASE_RE.search(label):
                if exclude_flag or (not one_year):
                    lt_end.setdefault("lease_ncl", v_end)
                else:
                    st_end.setdefault("lease", v_end)
                continue

            if _BOND_RE.search(label):
                if exclude_flag or (not one_year):
                    lt_end.setdefault("bond_ncl", v_end)
                else:
                    st_end.setdefault("cur_bond", v_end)
                continue

            if _LOAN_RE.search(label):
                if one_year and (not exclude_flag) and ("長期" in label or "ノンリコース" in label or "Nonrecourse" in label):
                    st_end.setdefault("cur_lt_loan", v_end)
                else:
                    if ("長期" in label) or exclude_flag:
                        lt_end.setdefault("long_loan", v_end)
                continue

        st_end_raw = sum(v for v in st_end.values() if v is not None) if st_end else None
        lt_end_raw = sum(v for v in lt_end.values() if v is not None) if lt_end else None

        if st_end_raw is None and lt_end_raw is None:
            continue

        score = 0
        score += 3 * len(st_end)
        score += 3 * len(lt_end)
        score += 2 if unit_explicit else 0

        out.append({
            "unit_hint": unit_hint,
            "unit_explicit": unit_explicit,
            "st_end_raw": st_end_raw,
            "lt_end_raw": lt_end_raw,
            "st_end_components": st_end,
            "lt_end_components": lt_end,
            "_score": score,
        })

    out.sort(key=lambda x: x.get("_score", 0), reverse=True)
    return out[:30]

_BOND_TABLE_HINT_RE = re.compile(r"(社債|転換社債|新株予約権付社債|CB|Bonds?)", re.IGNORECASE)

def extract_bond_maturity_1y_from_html(html: str) -> List[dict]:
    out = []
    if not html or "<table" not in html:
        return out
    soup = BeautifulSoup(html, "lxml")

    for tbl in soup.find_all("table"):
        tbl_text = tbl.get_text(" ", strip=True)
        # ★重要：社債テーブル以外（借入金の年限表など）を除外
        if not _BOND_TABLE_HINT_RE.search(tbl_text):
            continue

        matrix = []
        for tr in tbl.find_all("tr"):
            row = [c.get_text(" ", strip=True) for c in tr.find_all(["th", "td"])]
            if row:
                matrix.append(row)
        if not matrix:
            continue

        header_text = " ".join(matrix[0])
        if ("１年以内" not in header_text and "1年以内" not in header_text):
            continue
        if not _BOND_MATURITY_HDR_RE.search(" ".join(matrix[0])):
            continue

        unit_hint, unit_explicit = _detect_unit_and_explicit(" ".join(matrix[0]) + " " + tbl_text)

        col_1y = None
        for j, cell in enumerate(matrix[0]):
            if "１年以内" in cell or "1年以内" in cell:
                col_1y = j
                break
        if col_1y is None:
            continue

        total_val = None
        for r in matrix[1:]:
            if col_1y >= len(r):
                continue
            if r and ("合計" in r[0] or "Total" in r[0]):
                v = _parse_int_from_cell(r[col_1y])
                if v is not None:
                    total_val = v
                    break

        if total_val is None:
            s = 0
            cnt = 0
            for r in matrix[1:]:
                if col_1y >= len(r):
                    continue
                v = _parse_int_from_cell(r[col_1y])
                if v is None:
                    continue
                s += v
                cnt += 1
            if cnt <= 0:
                continue
            total_val = s

        yen = _raw_to_yen(total_val, unit_hint)
        if yen is None or yen <= 0:
            continue

        out.append({
            "raw_1y": total_val,
            "unit_hint": unit_hint,
            "unit_explicit": unit_explicit,
            "yen_1y": yen,
            "_score": (10 if unit_explicit else 0) + 5,
        })

    out.sort(key=lambda x: x.get("_score", 0), reverse=True)
    return out[:10]

