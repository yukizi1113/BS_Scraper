def is_financial_results_title(title: str) -> bool:
    t = (title or "").replace("\xa0", " ").replace("\u3000", " ").strip()
    if not t:
        return False
    return any(p.search(t) for p in _FIN_RESULTS_PATTERNS)


def tdnet_fetch(session: requests.Session, url: str, timeout: int = 30) -> str:
    r = session.get(url, timeout=timeout)
    r.raise_for_status()
    r.encoding = r.apparent_encoding or "utf-8"
    return r.text


def tdnet_iter_daily_xbrl_zip_links(session: requests.Session, day: dt.date, target_tickers: set[str], max_pages: int = 200) -> List[dict]:
    ymd = day.strftime("%Y%m%d")
    out: List[dict] = []

    def clean(s: str) -> str:
        return (s or "").replace("\xa0", " ").replace("\u3000", " ").strip()

    code_pat = re.compile(r"^\d{4}[0-9A-Z]$", re.IGNORECASE)
    time_pat = re.compile(r"^\d{2}:\d{2}$")

    for p in range(1, max_pages + 1):
        url = urljoin(TDNET_BASE, f"I_list_{p:03d}_{ymd}.html")
        try:
            html = tdnet_fetch(session, url)
        except requests.HTTPError:
            break

        soup = BeautifulSoup(html, "html.parser")
        found_any_zip_in_page = False

        for tr in soup.find_all("tr"):
            zips = [a for a in tr.find_all("a", href=True) if a["href"].lower().endswith(".zip")]
            if not zips:
                continue
            found_any_zip_in_page = True

            tds = [clean(td.get_text(" ", strip=True)) for td in tr.find_all("td")]
            time_txt = tds[0] if len(tds) > 0 else ""
            code_txt = tds[1] if len(tds) > 1 else ""
            title_txt = tds[3] if len(tds) > 3 else ""

            if not time_pat.match(time_txt):
                continue
            if not code_pat.match(code_txt):
                continue

            code4 = code_txt[:4]
            if code4 not in target_tickers:
                continue

            if not is_financial_results_title(title_txt):
                continue

            out.append({
                "time": time_txt,
                "code": code_txt,
                "title": title_txt,
                "zip_urls": [urljoin(TDNET_BASE, a["href"]) for a in zips]
            })

        if not found_any_zip_in_page:
            break

    expanded = []
    for r in out:
        for zu in r["zip_urls"]:
            rr = dict(r)
            rr["zip_url"] = zu
            expanded.append(rr)
    return expanded


def scrape_tdnet_bs(target_tickers: set[str], days_back: int = 35, sleep_sec: float = 1.0, max_pages: int = 200) -> Dict[str, Dict[dt.date, dict]]:
    session = requests.Session()
    session.headers.update({"User-Agent": TDNET_UA})

    today_jst = (dt.datetime.utcnow() + dt.timedelta(hours=9)).date()
    start = today_jst - dt.timedelta(days=days_back)

    store: Dict[str, Dict[dt.date, dict]] = defaultdict(dict)
    seen_zip = set()

    for i in range(days_back + 1):
        day = start + dt.timedelta(days=i)
        try:
            items = tdnet_iter_daily_xbrl_zip_links(session, day=day, target_tickers=target_tickers, max_pages=max_pages)
        except Exception:
            items = []

        for it in items:
            zip_url = it.get("zip_url")
            if not zip_url or zip_url in seen_zip:
                continue
            seen_zip.add(zip_url)

            code4 = str(it.get("code", ""))[:4]
            if code4 not in target_tickers:
                continue

            try:
                zb = session.get(zip_url, timeout=60).content
                extracted = process_one_zip(zb, doc_kind=None)
            except Exception:
                continue
            finally:
                time.sleep(sleep_sec + random.random() * 0.4)

            try:
                hh, mm = [int(x) for x in (it.get("time") or "00:00").split(":")]
                as_of = dt.datetime(day.year, day.month, day.day, hh, mm)
            except Exception:
                as_of = dt.datetime(day.year, day.month, day.day, 0, 0)

            for suffix in ("prior", "current"):
                dstr = extracted.get(f"bs_{suffix}_date")
                d = parse_date_any(dstr) if dstr else None
                if not d:
                    continue

                rec = {
                    "source": "TDNet",
                    "doc_kind": "OTHER",
                    "suffix": suffix,
                    "as_of": as_of,
                    "title": it.get("title", ""),
                    "zip_url": zip_url,
                    "fy_end_month": extracted.get("fy_end_month"),
                    "assets_total": yen_to_million_3dp(extracted.get(f"assets_total_{suffix}")),
                    "current_assets": yen_to_million_3dp(extracted.get(f"current_assets_{suffix}")),
                    "cash_eq_st_invest": yen_to_million_3dp(extracted.get(f"cash_eq_st_invest_{suffix}")),
                    "current_liabilities": yen_to_million_3dp(extracted.get(f"current_liabilities_{suffix}")),
                    "short_term_borrowings": yen_to_million_3dp(extracted.get(f"short_term_borrowings_{suffix}")),
                    "long_term_borrowings": yen_to_million_3dp(extracted.get(f"long_term_borrowings_{suffix}")),
                    "income_taxes_payable": yen_to_million_3dp(extracted.get(f"income_taxes_payable_{suffix}")),
                    "shares_outstanding": extracted.get(f"shares_outstanding_{suffix}"),
        "debt_ctx": extracted.get(f"debt_ctx_{suffix}"),
                    "debt_ctx": extracted.get(f"debt_ctx_{suffix}"),
                }
                rec["_completeness"] = completeness_score(rec)

                prev = store[code4].get(d)
                if better_than(rec, prev):
                    store[code4][d] = rec

    return store


