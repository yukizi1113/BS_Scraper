def _yen_unit_multiplier(unit_hint: str) -> int:
    if unit_hint == "百万円":
        return 1_000_000
    if unit_hint == "千円":
        return 1_000
    if unit_hint == "円":
        return 1
    return 1_000_000


def _component_dict_to_yen(comps_raw: dict, unit_hint: str) -> dict:
    mul = _yen_unit_multiplier(unit_hint)
    out = {}
    for k, v in (comps_raw or {}).items():
        if v is None:
            continue
        out[k] = int(v) * mul
    return out


def _close(a: Optional[int], b: Optional[int], tol: int = COMP_MATCH_TOL_YEN) -> bool:
    if a is None or b is None:
        return False
    return abs(int(a) - int(b)) <= tol


def _apply_html_additive_adjustment_for_asr(
    out: dict,
    v_full, v_local,
    debt_ctx: str,
    html_candidates: List[str],
) -> None:
    x = extract_debt_components_xbrl(v_full, v_local, debt_ctx)
    xbrl_st = x.get("st_total")
    xbrl_lt = x.get("lt_total")
    xbrl_loans_cl = x.get("loans_cl")
    xbrl_bonds_cl = x.get("bonds_cl")
    xbrl_cp_cl = x.get("cp_cl")
    xbrl_lease_cl = x.get("lease_cl")
    xbrl_loans_ncl = x.get("loans_ncl")
    xbrl_bonds_ncl = x.get("bonds_ncl")
    xbrl_lease_ncl = x.get("lease_ncl")

    schedules: List[dict] = []
    for h in html_candidates:
        schedules.extend(extract_debt_schedules_from_html(h))
    if not schedules:
        return

    st_sch = None
    lt_sch = None
    for sch in schedules:
        stc = sch.get("st_end_components") or {}
        ltc = sch.get("lt_end_components") or {}
        if st_sch is None and (("short_loan" in stc) or ("cur_lt_loan" in stc) or ("lease" in stc) or ("cp" in stc)):
            st_sch = sch
        if lt_sch is None and (("long_loan" in ltc) or ("lease_ncl" in ltc) or ("bond_ncl" in ltc)):
            lt_sch = sch
        if st_sch and lt_sch:
            break

    # Short-term
    if st_sch:
        unit_hint = st_sch.get("unit_hint", "百万円")
        st_html_yen = _component_dict_to_yen(st_sch.get("st_end_components", {}), unit_hint)
        html_loans_sum = sum_nullable(st_html_yen.get("short_loan"), st_html_yen.get("cur_lt_loan"))
        loans_sum_matches = _close(xbrl_loans_cl, html_loans_sum)

        missing_add = 0
        if st_html_yen.get("lease") not in (None, 0) and (xbrl_lease_cl in (None, 0)):
            missing_add += int(st_html_yen["lease"])
        if st_html_yen.get("cp") not in (None, 0) and (xbrl_cp_cl in (None, 0)):
            missing_add += int(st_html_yen["cp"])
        if st_html_yen.get("cur_bond") not in (None, 0) and (xbrl_bonds_cl in (None, 0)):
            missing_add += int(st_html_yen["cur_bond"])
        if (not loans_sum_matches) and st_html_yen.get("cur_lt_loan") not in (None, 0):
            if xbrl_loans_cl is None:
                missing_add += int(st_html_yen["cur_lt_loan"])

        if missing_add > 0:
            base = int(xbrl_st) if xbrl_st is not None else 0
            new_st = base + missing_add
            if xbrl_st is None or new_st <= int(xbrl_st) * HTML_ADD_MAX_RATIO_ST:
                cur_liab = first_value(v_full, v_local, BS_FIELDS["current_liabilities"], debt_ctx)
                if cur_liab is None or new_st <= int(cur_liab) + DEBT_SANITY_MARGIN_YEN:
                    out["short_term_borrowings_current"] = new_st

    # Long-term
    if lt_sch:
        unit_hint = lt_sch.get("unit_hint", "百万円")
        lt_html_yen = _component_dict_to_yen(lt_sch.get("lt_end_components", {}), unit_hint)

        missing_add = 0
        add_lease = (lt_html_yen.get("lease_ncl") not in (None, 0) and (xbrl_lease_ncl in (None, 0)))
        add_bond  = (lt_html_yen.get("bond_ncl") not in (None, 0) and (xbrl_bonds_ncl in (None, 0)))

        if add_lease:
            missing_add += int(lt_html_yen["lease_ncl"])
        if add_bond:
            missing_add += int(lt_html_yen["bond_ncl"])
        if lt_html_yen.get("long_loan") not in (None, 0) and xbrl_loans_ncl is None:
            missing_add += int(lt_html_yen["long_loan"])

        if missing_add > 0:
            base = int(xbrl_lt) if xbrl_lt is not None else 0
            new_lt = base + missing_add
            max_ratio = HTML_ADD_MAX_RATIO_LT_RELAXED if (add_lease or add_bond) else HTML_ADD_MAX_RATIO_LT_DEFAULT
            if xbrl_lt is None or new_lt <= int(xbrl_lt) * max_ratio:
                out["long_term_borrowings_current"] = new_lt

    # Bond maturity schedule 1y add（cur_bond が無い場合のみ）
    cur_bond_present = (xbrl_bonds_cl not in (None, 0))
    if st_sch:
        stc = st_sch.get("st_end_components") or {}
        if stc.get("cur_bond") not in (None, 0):
            cur_bond_present = True

    if not cur_bond_present:
        bond_cands: List[dict] = []
        for h in html_candidates:
            bond_cands.extend(extract_bond_maturity_1y_from_html(h))
        if bond_cands:
            bond_1y_yen = bond_cands[0].get("yen_1y")
            if isinstance(bond_1y_yen, int) and bond_1y_yen > 0:
                cur_st = out.get("short_term_borrowings_current")
                if cur_st is None:
                    out["short_term_borrowings_current"] = bond_1y_yen
                else:
                    out["short_term_borrowings_current"] = int(cur_st) + int(bond_1y_yen)

    # --- Prefer schedule totals when the selected debt_ctx is nonconsolidated ---
    # Some filers tag debt only in NonConsolidatedMember contexts, while the schedule table is consolidated.
    # In that case, schedule totals are usually closer to what the Excel expects.
    if "nonconsolidated" in (debt_ctx or "").lower():
        if st_sch:
            unit_hint = st_sch.get("unit_hint", "百万円")
            st_total_yen = _raw_to_yen(st_sch.get("st_end_raw"), unit_hint)
            if isinstance(st_total_yen, int) and st_total_yen >= 0:
                cur_liab = first_value(v_full, v_local, BS_FIELDS["current_liabilities"], debt_ctx)
                if cur_liab is None or st_total_yen <= int(cur_liab) + DEBT_SANITY_MARGIN_YEN:
                    out["short_term_borrowings_current"] = st_total_yen

        if lt_sch:
            unit_hint = lt_sch.get("unit_hint", "百万円")
            lt_total_yen = _raw_to_yen(lt_sch.get("lt_end_raw"), unit_hint)
            if isinstance(lt_total_yen, int) and lt_total_yen >= 0:
                out["long_term_borrowings_current"] = lt_total_yen

