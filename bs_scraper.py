def to_decimal_safe(v: object) -> Optional[Decimal]:
    if v is None:
        return None
    if isinstance(v, Decimal):
        return v
    if isinstance(v, int):
        return Decimal(v)
    if isinstance(v, float):
        return Decimal(str(v))
    s = str(v).strip()
    if not s:
        return None
    s = s.replace(",", "")
    try:
        return Decimal(s)
    except Exception:
        return None


def has_fraction_3dp(x: float) -> bool:
    d = Decimal(str(x)).quantize(Decimal("0.001"), rounding=ROUND_HALF_UP)
    return d != d.to_integral_value(rounding=ROUND_HALF_UP)


