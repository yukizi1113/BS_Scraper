def choose_bs_contexts(
    facts: List[Fact],
    ctx_info: Dict[str, dict],
    concept_pool_full: set,
    concept_pool_local: set,
) -> Tuple[Optional[str], Optional[str], Optional[str], Optional[str]]:
    def is_relevant(f: Fact) -> bool:
        if f.kind not in {"ix_nonFraction", "xbrl_numeric"}:
            return False
        return (f.name in concept_pool_full) or (local_name(f.name) in concept_pool_local)

    relevant_ctx = [f.ctx for f in facts if f.ctx and is_relevant(f)]
    used = set(relevant_ctx) if relevant_ctx else set(f.ctx for f in facts if f.ctx)

    freq = Counter([f.ctx for f in facts if f.ctx in used])

    instants = defaultdict(list)
    for cid in used:
        info = ctx_info.get(cid)
        if info and info.get("type") == "instant":
            instants[info.get("instant_date")].append(cid)

    dates = sorted([d for d in instants.keys() if d is not None])
    if not dates:
        return None, None, None, None

    cur_date = dates[-1]
    prior_date = dates[-2] if len(dates) >= 2 else None

    def pick(d: Optional[dt.date]) -> Optional[str]:
        if d is None:
            return None
        cands = instants.get(d, [])
        if not cands:
            return None

        def score(cid: str):
            base = 1 if "member" not in (cid or "").lower() else 0
            return (base, freq.get(cid, 0))

        return max(cands, key=score)

    return (
        prior_date.isoformat() if isinstance(prior_date, dt.date) else None,
        pick(prior_date),
        cur_date.isoformat(),
        pick(cur_date),
    )


def facts_to_value_maps(
    facts: List[Fact],
) -> Tuple[Dict[Tuple[str, str], Optional[int]], Dict[Tuple[str, str], Optional[int]]]:
    v_full: Dict[Tuple[str, str], Optional[int]] = {}
    v_local: Dict[Tuple[str, str], Optional[int]] = {}

    for f in facts:
        if not f.ctx:
            continue
        if f.kind == "ix_nonFraction":
            val = parse_value_ixbrl(f.txt, f.scale, f.sign)
        elif f.kind == "xbrl_numeric":
            val = parse_value_xbrl(f.txt)
        else:
            continue

        kf = (f.name, f.ctx)
        if kf not in v_full:
            v_full[kf] = val

        kl = (local_name(f.name), f.ctx)
        if kl not in v_local:
            v_local[kl] = val

    return v_full, v_local


